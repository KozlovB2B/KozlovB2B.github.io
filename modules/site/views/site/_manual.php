                <h1 id="how-to-call" class="page-header"> How to call using the script </h1>
                <ol>

                    <li>In order to make calls through the script, pass your operator/sales representative a username (looks like op1285@scriptdesigner.ru) and password</li>
                    <li>Operator/sales representative need to enter his username and password on the main page in the login section.</li>
                    <li>Operator/sales representative will see the list of published scripts (scripts in "Draft" state are not displayed). To start work with the script one must press the button with a telephone handset.</li>
                    <li>The script starts from the old window, where you had to press the "Picked up" button.<br/>

                        <a href="/wiki/img/en-US/3.png" target="_blank"><img class='img-responsive' src='/wiki/img/en-US/3.png'></a>
                    </li>

                    <li>Sales representative reads phrases of the script, selects his client's answers, presses the button with needed option and proceeds to the next node. After the completion of the call or no answer from the customer, sales representative presses the "End Call" button.</li>
                    <li>In the resulting window one has to select the applicable option in the "Aim reached" drop-down list. This information is used in reports on the scripts and calls.

                        <br/>

                        <a href="/wiki/img/en-US/4.png" target="_blank"><img class='img-responsive' src='/wiki/img/en-US/4.png'></a>
                    </li>

                    <li>Select the option "Call script quality control" - "Abnormal termination" and add comments. For example, the answer option, which was not in the script.  This information is used in reports on the scripts and calls.</li>
                    <li>This call will be displayed as Completed in the system. Next, press the button with a headset and begin a new call.</li>
                </ol>
            </div>
