                <h1 id="how-to-create-script" class="page-header">
                    How to create a script using a template
                </h1>

                <ol>

                    <li>Download a template file with needed script example with .SCRD extension by the link or in our group on Facebook https://www.facebook.com/groups/SalesScriptPROMPTER.</li>
                    <li>Go to the "Scripts" menu item</li>
                    <li>Press the "IMPORT FROM FILE" button in the top left corner.</li>
                    <li>Select the file with script template (*.SCRD).</li>
                    <li>File for import line will contain the full path to the file. Press "Import".</li>
                    <li>The loaded script will open. Each script consists of nodes, which are interconnected with each other. A node contains the phrase, which is said by a sales representative (top blue part of the node) and a client response options (green lines under the sales representative phrase). Each client
                        answer option is connected with a particular phrase of a sales representative (other node).
                        Use your mouse scroll button to enlarge the script.
                    </li>
                    <li>Select the node to be edited and click the button with a pencil (top right corner of the node).</li>
                    <li>A window with the script node opens. Make the necessary adjustments to the node text and press "OK".</li>
                    <li>To add an additional response options press "+Add a new answer".</li>
                    <li>A new answer option window appears. Add the new response (answer) and press "OK".</li>
                    <li>This new answer will be added to the existing options in the node. You can edit any client answer by pressing a pencil icon on the right of the text. If you want to delete an answer - press the X on the left of the text.</li>
                    <li>To create a new node hold the "ADD A NODE" button (top left part of the interface) and drag to the right.</li>
                    <li>In the resulting window you will be able to add a new sales representative phrase.</li>
                    <li>Select the call stage from the list (for convenience purposes nodes are linked to stages).</li>
                    <li>Name of the stage will be displayed in the "Conversation (calls) stage" line and press "OK".
                        <br/>
                        <a href="/wiki/img/en-US/1.png" target="_blank"><img class='img-responsive' src='/wiki/img/en-US/1.png'></a>
                    </li>


                    <li>New script node will appear on the screen. Press the centre of the line with a customer answer and pull the red line to the needed node in order to connect this node to the customer response option in the other node. Pull the red line to the top of the node until the blue part becomes green.
                        That means that the connection between the nodes was successfully established.
                    </li>
                    <li>From the list in the "Start" line (to the top left of the "Save" button) select the node, which should be launched at the start of the script.</li>
                    <li>Starting node will be displayed in the "Start" line.</li>
                    <li>Select an option in the "Operator interface" section (right, left, and bottom) to place the buttons with clients' answers or links.</li>
                    <li>If you want Operators/Sales sales representatives to see this node, select "Published" in the "State" line and press the "Save" button.</li>
                    <li>To view how the script works press the "TRY A CALL" button. In the resulting window, press the "Picked up" button.</li>
                    <li>You will see the script in work. Sales representatives/operators will see it in another interface. Press the client answer option and proceed to the next phrases. At the end press "End call".</li>
                    <li>The "Call ended" window will appear.</li>
                    <li>Go to the "Scripts" menu and you will see the list of scripts.<br/>
                        To view a script press the button with a handset.<br/>
                        To continue editing the script press the button with a pencil.<br/>
                        To save the script in the SCRD-format press the button with a floppy disk.<br/>
                        To delete the scripts press the button with a trash bin.
                    </li>


            </div>
            <div class="ss-instruction-section">
                <h1 id="create-script-from-scratch" class="page-header">
                    How to create a script from scratch
                </h1>

                <ol>

                    <li>Go to the "Scripts" menu.</li>
                    <li>Press the "Create script" button in the top left corner.</li>
                    <li>A blank script window opens.</li>
                    <li>Each script consists of nodes that are interconnected with each other. A node contains the phrase, which is said by a sales representative (top blue part of the node) and a client response options (green lines under the sales representative phrase). Each client answer option is connected with a
                        particular phrase of a sales representative (other node).
                    </li>
                    <li>To create a new node hold the "ADD A NODE" button and drag it to the right.</li>
                    <li>Opens the node window, where you have to write the objective of the call and the first phrase of the sales sales representative.</li>
                    <li>Select the conversation stage from the list.</li>
                    <li>Selected stage of the conversation will be displayed in the designated line; press "OK".</li>
                    <li>The window will not display the script node with the phrase of the sales representative.</li>
                    <li>Click the "+Add a new answer" line.</li>
                    <li>In the resulting window input a customer's response and press "OK".</li>
                    <li>Response will appear under the node.</li>
                    <li>Create a new script node by pressing and dragging the "ADD A NODE" button to the right.</li>
                    <li>Add a sales representative phrase and press "OK".</li>
                    <li>Press the centre of the line with a customer answer and pull the red line to the needed node in order to connect this node to the customer response option in the other node.</li>
                    <li>Pull the red line to the top of the node until the blue part becomes green. That means that the connection between the nodes was successfully established.</li>
                    <li>Enter the script's name into the "Name of script" field.</li>
                    <li>Select an option in the "Operator interface" section (right, left, and bottom) to place the buttons with clients' answers or links.</li>
                    <li>Select the node, which will be the first in this scrip from the list in the "Start" line and press "Save".</li>
                    <li>Select the "Published" state, so that operators/sales representatives could work with the script. Operators/sales representatives will not be able to see a script with the "Draft" state.</li>
                    <li>To view how the script works press the "TRY A CALL" button. In the resulting window press the "Picked up" button.</li>
                    <li>You will see the script in work. Sales representatives/operators will see it in another interface. Press the client answer option and proceed to the next phrases. At the end press "End call".</li>
                    <li>The "Call ended" window will appear.</li>
                    <li>Go to the "Scripts" menu and you will see the list of scripts.<br/>
                        To view the script, click on the button with a handset.<br/>
                        To continue editing the script, click on the button with a pencil.<br/>
                        To save the script in the SCRD format, click the button with a floppy disk.<br/>
                        To delete the script, click on the button with a trash bin.
                    </li>
            </div>


