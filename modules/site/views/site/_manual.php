                <h1 id="how-add-op" class="page-header">How to add an operator (sales representative) </h1>

                <ol>
                    <li>Go to the "Settings" menu and proceed to the "Operators" drop-down section.</li>
                    <li>Click the "Add operator" button</li>
                    <li>The resulting window shall specify username and password for the operator/sales representative login.</li>
                    <li>Now the new operator appears in the list of operators.</li>
                    <li>To edit the password click on the button with a pencil.</li>
                    <li>In the resulting window input a new password and press "Save".
