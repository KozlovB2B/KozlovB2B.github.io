                <h1 id="reports" class="page-header">How to evaluate the effectiveness of the operation by the script and work with the reports</h1>
                <ol>
                    <li>Go to the "Reports" menu item and select either "Scripts" or "Calls" item.</li>
                    <li>In the "Scripts" menu you will see the data on the work by scripts.</li>
                    <li>You can filter the data by operator, by script, by period just by pressing the "Search" button. You can also export the data in Excel. The number of calls, where the aim was reached, serve as an indicator of the effectiveness of the script.</li>
                    <li>By clicking on any number in a Script report, you will go to the list of calls, which corresponds to the specified characteristics (for example, "Abnormal termination"). This information is needed to make adjustments in the script.
                        <br/>

                        <a href="/wiki/img/en-US/5.png" target="_blank"><img class='img-responsive' src='/wiki/img/en-US/5.png'></a>
                    </li>
                    <li>In the By calls report you can search the calls by the specific characteristics: operator, script, aim, period. In addition, you can export the data into Excel. </li>
                </ol>
