                <h1 id="how-to-call" class="page-header">Как звонить по скрипту</h1>
                <ol>
                    <li>
                        Операторы регистрируются по приглашениям и знают свой логин и пароль (указывают его при принятии приглашения)
                    </li>
                    <li>
                        Оператору/ менеджеру на главной странице в разделе «Вход» нужно ввести логин и пароль<br/>
                        <a href="/wiki/img/big/image052.png" target="_blank"><img class='img-responsive' src='/wiki/img/big/image052.png'></a>
                    </li>
                    <li>
                        Оператор/ менеджер увидит список скриптов. Для начала работы со скриптом нужно нажать на кнопку с изображением телефонной трубки.<br/>
                        <a href="/wiki/img/big/image053.png" target="_blank"><img class='img-responsive' src='/wiki/img/big/image053.png'></a>
                    </li>
                    <li>
                        Скрипт начинается со стартового окна, в котором нужно нажать кнопку «Сняли трубку»<br/>
                        <a href="/wiki/img/big/image54.png" target="_blank"><img class='img-responsive' src='/wiki/img/big/image54.png'></a>
                    </li>
                    <li>
                        Менеджер читает фразы скрипта, выбирает ответы клиентов, нажимает кнопку с нужным ответом и переходит к следующему узлу.
                        После завершения звонка или отсутствия ответа клиента в скрипте менеджер нажимает кнопку «Завершить звонок».<br/>
                    </li>
                    <li>
                        В открывшемся окне нужно выбрать вариант в строке «Цель достигнута». Данная информация используется в отчетах по скриптам и звонкам.
                        Выбрать вариант «Звонок по скрипту/ нет» и внести комментарии. Например, вариант ответа, которого не было в скрипте.
                        Данная информация используется в отчетах по скриптам и звонкам. После сохранения результатов текущего звонка (кнопка «Сохранить»)
                        начинается следующий звонок.<br/>
