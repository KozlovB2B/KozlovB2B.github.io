                <h1 id="how-to-create-script" class="page-header">
                    Как создать скрипт
                </h1>

                <p class="lead">Вариант 1 &mdash; Использовать шаблон скрипта</p>
                <ol>
                    <li>Скачайте шаблоны скриптов с расширением .SCRD здесь b2bbasis.info/script (в статьях есть ссылки на файлы скриптов) или здесь <a href='https://www.facebook.com/groups/ScriptDesigner' target='_blank'>https://www.facebook.com/groups/ScriptDesigner</a>.</li>
                    <li>
                        Зайдите в свою учетную запись.<br/>
                        <a href="/wiki/img/big/image001.png" target="_blank"><img class='img-responsive' src='/wiki/img/big/image001.png'></a>
                    </li>
                    <li>
                        Нажмите на кнопку «Импортировать из файла» вверхнем левом углу<br/>
                        <a href="/wiki/img/big/image002.png" target="_blank"><img class='img-responsive' src='/wiki/img/big/image002.png'></a>
                    </li>
                    <li>
                        Перетащите файл скрипта в помеченную пунктиром область или кликните на область мышью.<br/>
                        <a href="/wiki/img/big/image003.png" target="_blank"><img class='img-responsive' src='/wiki/img/big/image003.png'></a>
                    </li>
                    <li>
                        Выберите в открывшемся окне местоположение файла с вашим скриптом и откройте его<br/>
                        <a href="/wiki/img/big/image004.png" target="_blank"><img class='img-responsive' src='/wiki/img/big/image004.png'></a>
                    </li>
                    <li>
                        Откроется дерево заруженного скрипта. Каждый скрипт состоит из узлов, которые соединены друг с другом связями. В узел входит фраза, которую произносит менеджер (верхняя синяя часть узла) и варианты ответов клиента (зеленые строки под фразой менеджера). Каждый вариант ответа клиента связан с фразой менеджера (другим узлом).
                        Чтобы сделать скрипт крупнее покрутите колечко мышки.<br/>
                        <a href="/wiki/img/big/image005.png" target="_blank"><img class='img-responsive' src='/wiki/img/big/image005.png'></a>
                    </li>
                    <li>
                        Выберите узел для внесения корректив и нажмите изображение карандаша - выделено оранжевым цветом с подсказкой «Редактировать содержимое».<br/>
                        <a href="/wiki/img/big/image006.png" target="_blank"><img class='img-responsive' src='/wiki/img/big/image006.png'></a>
                    </li>
                    <li>
                        Откроется окна с узлом скрипта. Внесите коррективы в текст узла с помощью функции текстового редактора и нажмите кнопку «ОК»<br/>
                        <a href="/wiki/img/big/image007.png" target="_blank"><img class='img-responsive' src='/wiki/img/big/image007.png'></a>
                    </li>
                    <li>
                        Для того, чтобы добавить дополнительные вариант ответа нажмите на поле «+Добавить ответ»<br/>
                        <a href="/wiki/img/big/image008.png" target="_blank"><img class='img-responsive' src='/wiki/img/big/image008.png'></a>
                    </li>
                    <li>
                        Откроется окно варианта ответа. Внесите вариант ответа и нажмите кнопку «ОК».
                    </li>
                    <li>
                        Новый вариант ответа будет добавлен к существующим ответам в узле. Отредактировать ответ клиента можно, нажав на карандаш справа от текст,
                        удалить ответ - нажав на крест слева от текста.
                        <br/>
                        <a href="/wiki/img/big/image009.png" target="_blank"><img class='img-responsive' src='/wiki/img/big/image009.png'></a>
                    </li>
                    <li>
                        Для создания нового узла захватите мышью кнопку «Добавить узел» и потяните вправо - откроется окно.<br/>
                        <a href="/wiki/img/big/image010.png" target="_blank"><img class='img-responsive' src='/wiki/img/big/image010.png'></a>
                    </li>
                    <li>
                        Впишите в него фразу менеджера. <br/>
                        <a href="/wiki/img/big/image011.png" target="_blank"><img class='img-responsive' src='/wiki/img/big/image011.png'></a>
                    </li>
                    <li>
                        Выберите этап разговора из списка (для удобства привязки узлов к этапам).<br/>
                        <a href="/wiki/img/big/image012.png" target="_blank"><img class='img-responsive' src='/wiki/img/big/image012.png'></a>
                    </li>
                    <li>
                        Название этапа отобразиться в строке и нажмите «ОК».<br/>
                        <a href="/wiki/img/big/image013.png" target="_blank"><img class='img-responsive' src='/wiki/img/big/image013.png'></a>
                    </li>
                    <li>
                        Новый узел появиться на экране. Для того, чтобы соединить его с вариантом ответа клиента в другом узле, нажмите на центр строки
                        с ответом клиента и потяните красную линию к нужному узлу.<br/>
                        <a href="/wiki/img/big/image014.png" target="_blank"><img class='img-responsive' src='/wiki/img/big/image014.png'></a>
                    </li>
                    <li>
                        Подтяните красную линию к верхней части узла до момента, как синия часть станет зеленой. Это значит, что связь между узлами установлена.<br/>
                        <a href="/wiki/img/big/image015.png" target="_blank"><img class='img-responsive' src='/wiki/img/big/image015.png'></a>
                    </li>
                    <li>
                        Выберите в строке Начало из списка узел, с которого должен стартовать скрипт.
                        Стартовый узел отобразиться в строке под наименованием скрипта. Состояние скрипта сохраняется в облачном хранилище автоматически.
                        <br/>
                        <a href="/wiki/img/big/image016.png" target="_blank"><img class='img-responsive' src='/wiki/img/big/image016.png'></a>
                    </li>

                    <li>
                        Для того, чтобы посмотреть скрипт в работе нажмите кнопку «Пробный звонок». В открывшемся окне нажмите на кнопку «Снять трубку»<br/>
                        <a href="/wiki/img/big/image017.png" target="_blank"><img class='img-responsive' src='/wiki/img/big/image017.png'></a>
                    </li>
                    <li>
                        Вы увидите скрипт в том виде, в котором с ним будут работать операторы/ менеджеры по продажам. Нажимайте на варианты ответов клиента и переходите к следующим фразам. В конце нажмите «Завершение звонка»<br/>
